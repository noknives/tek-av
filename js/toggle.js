// Flipper 1

$(function() {
    $( "#front-1" ).click(function() {
      $( "#flipper-1" ).addClass( "flipped", 1000 );
    });
  });

$(function() {
    $( "#back-1" ).click(function() {
      $( "#flipper-1" ).removeClass( "flipped", 1000 );
    });
  });


